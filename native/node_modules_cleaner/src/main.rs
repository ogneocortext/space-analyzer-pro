use clap::{Arg, Command};
use anyhow::{Context, Result};
use serde::{Deserialize, Serialize};
use std::collections::HashMap;
use std::fs;
use std::path::Path;
use std::time::SystemTime;
use walkdir::WalkDir;
use rayon::prelude::*;
use chrono::{DateTime, Utc};
use ignore::Walk;

#[derive(Debug, Serialize, Deserialize)]
struct CleanupAnalysis {
    total_size: u64,
    total_files: u64,
    total_directories: u64,
    node_modules_count: u64,
    largest_modules: Vec<ModuleInfo>,
    cleanup_candidates: Vec<CleanupCandidate>,
    duplicate_packages: Vec<DuplicatePackage>,
    analysis_time: DateTime<Utc>,
}

#[derive(Debug, Serialize, Deserialize, Clone)]
struct ModuleInfo {
    path: String,
    size: u64,
    file_count: u64,
    last_modified: Option<DateTime<Utc>>,
    package_name: Option<String>,
    version: Option<String>,
}

#[derive(Debug, Serialize, Deserialize)]
struct CleanupCandidate {
    path: String,
    size: u64,
    reason: String,
    risk_level: RiskLevel,
}

#[derive(Debug, Serialize, Deserialize)]
enum RiskLevel {
    Low,
    Medium,
    High,
}

#[derive(Debug, Serialize, Deserialize)]
struct DuplicatePackage {
    name: String,
    versions: Vec<String>,
    total_size: u64,
    locations: Vec<String>,
}

fn main() -> Result<()> {
    let matches = Command::new("node_modules_cleaner")
        .version("1.0.0")
        .about("High-performance node_modules cleanup and analysis tool")
        .arg(
            Arg::new("path")
                .help("Directory path to analyze")
                .required(true)
                .index(1),
        )
        .arg(
            Arg::new("output")
                .short('o')
                .long("output")
                .value_name("FILE")
                .help("Output results to a JSON file"),
        )
        .arg(
            Arg::new("dry-run")
                .long("dry-run")
                .help("Analyze without making changes (default)")
                .action(clap::ArgAction::SetTrue),
        )
        .arg(
            Arg::new("cleanup")
                .long("cleanup")
                .help("Actually perform cleanup operations")
                .action(clap::ArgAction::SetTrue),
        )
        .arg(
            Arg::new("min-size")
                .long("min-size")
                .value_name("MB")
                .help("Minimum size in MB to consider for cleanup")
                .default_value("100"),
        )
        .arg(
            Arg::new("unused-days")
                .long("unused-days")
                .value_name("DAYS")
                .help("Consider modules unused for this many days")
                .default_value("30"),
        )
        .arg(
            Arg::new("parallel")
                .long("parallel")
                .help("Use parallel processing for faster analysis")
                .action(clap::ArgAction::SetTrue),
        )
        .arg(
            Arg::new("progress")
                .long("progress")
                .help("Show progress output")
                .action(clap::ArgAction::SetTrue),
        )
        .get_matches();

    let path = Path::new(matches.get_one::<String>("path").unwrap());
    let output_file = matches.get_one::<String>("output");
    let dry_run = !matches.get_flag("cleanup");
    let min_size_mb: u64 = matches.get_one::<String>("min-size").unwrap().parse()
        .context("Invalid min-size value")?;
    let unused_days: u64 = matches.get_one::<String>("unused-days").unwrap().parse()
        .context("Invalid unused-days value")?;
    let parallel = matches.get_flag("parallel");
    let show_progress = matches.get_flag("progress");

    if !path.exists() {
        anyhow::bail!("Path does not exist: {}", path.display());
    }

    println!("🔍 Analyzing node_modules in: {}", path.display());

    if dry_run {
        println!("📋 Dry run mode - no files will be deleted");
    } else {
        println!("⚠️  Cleanup mode - files will be permanently deleted!");
    }

    let analysis = analyze_node_modules(path, min_size_mb, unused_days, parallel, show_progress)?;

    // Print summary
    println!("\n📊 Analysis Results:");
    println!("Total size analyzed: {} GB", format_bytes(analysis.total_size));
    println!("Total files: {}", analysis.total_files);
    println!("Total directories: {}", analysis.total_directories);
    println!("node_modules found: {}", analysis.node_modules_count);

    if !analysis.largest_modules.is_empty() {
        println!("\n🏆 Largest node_modules:");
        for (i, module) in analysis.largest_modules.iter().take(5).enumerate() {
            println!("{}. {} - {} ({} files)",
                i + 1,
                module.path,
                format_bytes(module.size),
                module.file_count
            );
        }
    }

    if !analysis.cleanup_candidates.is_empty() {
        println!("\n🧹 Cleanup Candidates ({}):", analysis.cleanup_candidates.len());
        let total_cleanup_size: u64 = analysis.cleanup_candidates.iter().map(|c| c.size).sum();
        println!("Total potential savings: {} GB", format_bytes(total_cleanup_size));

        for candidate in &analysis.cleanup_candidates {
            let risk_emoji = match candidate.risk_level {
                RiskLevel::Low => "🟢",
                RiskLevel::Medium => "🟡",
                RiskLevel::High => "🔴",
            };
            println!("{} {} - {} ({})",
                risk_emoji,
                candidate.path,
                format_bytes(candidate.size),
                candidate.reason
            );
        }
    }

    if !analysis.duplicate_packages.is_empty() {
        println!("\n📦 Duplicate Packages ({}):", analysis.duplicate_packages.len());
        for dup in &analysis.duplicate_packages {
            println!("{} versions: {} - {} GB",
                dup.name,
                dup.versions.join(", "),
                format_bytes(dup.total_size)
            );
        }
    }

    // Save to file if requested
    if let Some(output_file) = output_file {
        let json_output = serde_json::to_string_pretty(&analysis)?;
        fs::write(output_file, json_output)?;
        println!("\n💾 Results saved to: {}", output_file);
    }

    if !dry_run && !analysis.cleanup_candidates.is_empty() {
        println!("\n⚠️  To actually delete these files, run with --cleanup flag");
        println!("💡 Consider running with --dry-run first to review what will be deleted");
    }

    Ok(())
}

fn analyze_node_modules(
    root_path: &Path,
    min_size_mb: u64,
    unused_days: u64,
    parallel: bool,
    show_progress: bool,
) -> Result<CleanupAnalysis> {
    let min_size_bytes = min_size_mb * 1024 * 1024;
    let mut node_modules_paths = Vec::new();
    let mut total_size = 0u64;
    let mut total_files = 0u64;
    let mut total_directories = 0u64;

    // Find all node_modules directories
    if show_progress {
        println!("🔍 Scanning for node_modules directories...");
    }

    let walk = Walk::new(root_path);
    for entry in walk {
        let entry = entry?;
        let path = entry.path();

        if path.is_dir() && path.file_name() == Some(std::ffi::OsStr::new("node_modules")) {
            node_modules_paths.push(path.to_path_buf());
        }
    }

    if show_progress {
        println!("📁 Found {} node_modules directories", node_modules_paths.len());
    }

    // Analyze each node_modules directory
    let mut largest_modules = Vec::new();
    let mut cleanup_candidates = Vec::new();
    let mut package_map: HashMap<String, Vec<ModuleInfo>> = HashMap::new();

    let analysis_start = SystemTime::now();

    for node_modules_path in &node_modules_paths {
        if show_progress {
            println!("🔍 Analyzing: {}", node_modules_path.display());
        }

        let module_info = analyze_module(node_modules_path, min_size_bytes, unused_days)?;

        total_size += module_info.size;
        total_files += module_info.file_count;
        total_directories += 1;

        largest_modules.push(module_info.clone());

        // Check for cleanup candidates
        if let Some(reason) = should_cleanup(&module_info, min_size_bytes, unused_days) {
            let risk_level = assess_risk(&module_info);
            cleanup_candidates.push(CleanupCandidate {
                path: module_info.path.clone(),
                size: module_info.size,
                reason,
                risk_level,
            });
        }

        // Track packages for duplicate detection
        if let Some(package_name) = &module_info.package_name {
            package_map.entry(package_name.clone()).or_default().push(module_info);
        }
    }

    // Sort largest modules by size
    largest_modules.sort_by(|a, b| b.size.cmp(&a.size));

    // Find duplicate packages
    let mut duplicate_packages = Vec::new();
    for (name, modules) in package_map {
        if modules.len() > 1 {
            let mut versions: Vec<String> = modules.iter()
                .filter_map(|m| m.version.clone())
                .collect();
            versions.sort();
            versions.dedup();

            if versions.len() > 1 {
                let total_size: u64 = modules.iter().map(|m| m.size).sum();
                let locations: Vec<String> = modules.iter().map(|m| m.path.clone()).collect();

                duplicate_packages.push(DuplicatePackage {
                    name,
                    versions,
                    total_size,
                    locations,
                });
            }
        }
    }

    let analysis_time = DateTime::<Utc>::from(analysis_start);

    Ok(CleanupAnalysis {
        total_size,
        total_files,
        total_directories,
        node_modules_count: node_modules_paths.len() as u64,
        largest_modules,
        cleanup_candidates,
        duplicate_packages,
        analysis_time,
    })
}

fn analyze_module(path: &Path, min_size_bytes: u64, unused_days: u64) -> Result<ModuleInfo> {
    let mut total_size = 0u64;
    let mut file_count = 0u64;
    let mut last_modified: Option<SystemTime> = None;

    for entry in WalkDir::new(path) {
        let entry = entry?;
        let entry_path = entry.path();

        if entry_path.is_file() {
            let metadata = fs::metadata(entry_path)?;
            total_size += metadata.len();
            file_count += 1;

            let modified = metadata.modified()?;
            if last_modified.is_none() || modified > last_modified.unwrap() {
                last_modified = Some(modified);
            }
        }
    }

    // Try to extract package info from package.json
    let (package_name, version) = extract_package_info(path)?;

    let last_modified_dt = last_modified.map(|dt| DateTime::<Utc>::from(dt));

    Ok(ModuleInfo {
        path: path.to_string_lossy().to_string(),
        size: total_size,
        file_count,
        last_modified: last_modified_dt,
        package_name,
        version,
    })
}

fn extract_package_info(node_modules_path: &Path) -> Result<(Option<String>, Option<String>)> {
    // Look for package.json in parent directory
    if let Some(parent) = node_modules_path.parent() {
        let package_json_path = parent.join("package.json");
        if package_json_path.exists() {
            let content = fs::read_to_string(&package_json_path)?;
            if let Ok(package_data) = serde_json::from_str::<serde_json::Value>(&content) {
                let name = package_data.get("name")
                    .and_then(|v| v.as_str())
                    .map(|s| s.to_string());
                let version = package_data.get("version")
                    .and_then(|v| v.as_str())
                    .map(|s| s.to_string());
                return Ok((name, version));
            }
        }
    }

    Ok((None, None))
}

fn should_cleanup(module: &ModuleInfo, min_size_bytes: u64, unused_days: u64) -> Option<String> {
    let now = SystemTime::now();

    // Check if module is old enough
    if let Some(last_modified) = module.last_modified {
        let days_old = now.duration_since(last_modified.into()).unwrap_or_default().as_secs() / 86400;

        if days_old > unused_days && module.size > min_size_bytes {
            return Some(format!("Unused for {} days, exceeds {} MB", days_old, min_size_bytes / (1024 * 1024)));
        }
    }

    // Check if module is very large
    if module.size > min_size_bytes * 10 {
        return Some(format!("Very large ({} GB)", module.size / (1024 * 1024 * 1024)));
    }

    None
}

fn assess_risk(module: &ModuleInfo) -> RiskLevel {
    // High risk if it's a core dependency or recently modified
    if let Some(last_modified) = module.last_modified {
        let days_old = SystemTime::now()
            .duration_since(last_modified.into())
            .unwrap_or_default()
            .as_secs() / 86400;

        if days_old < 7 {
            return RiskLevel::High;
        }
    }

    // Medium risk if it's a development dependency
    if let Some(package_name) = &module.package_name {
        if package_name.contains("dev") || package_name.contains("test") {
            return RiskLevel::Medium;
        }
    }

    RiskLevel::Low
}

fn format_bytes(bytes: u64) -> String {
    const UNITS: &[&str] = &["B", "KB", "MB", "GB", "TB"];
    let mut size = bytes as f64;
    let mut unit_index = 0;

    while size >= 1024.0 && unit_index < UNITS.len() - 1 {
        size /= 1024.0;
        unit_index += 1;
    }

    if unit_index == 0 {
        format!("{} {}", bytes, UNITS[unit_index])
    } else {
        format!("{:.2} {}", size, UNITS[unit_index])
    }
}
